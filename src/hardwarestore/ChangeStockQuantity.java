    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
            
        String addOrRemove = jComboBox1.getSelectedItem().toString();
               
        if(addOrRemove == "Add"){       
            amount = amount + Double.parseDouble(jTextField6.getText());
        }
        else if(addOrRemove == "Remove"){
            amount = amount - Double.parseDouble(jTextField6.getText());
        }
        else{
            JOptionPane.showMessageDialog(this, "Programme Update Error! ");
        }
        
        try {
           
            pst = con.prepareStatement("delete from stock where item_no = ?");
            pst.setString(1, itemNo);
            pst.executeUpdate();
            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Please choose a product! ");
        }
        finally{
            try {
                pst = con.prepareStatement("insert into stock(item_no, description, quantity_available, unit_price) values(?,?,?,?)");
                pst.setString(1, itemNo);
                pst.setString(2, description);
                pst.setString(3, Double.toString(amount));
                pst.setString(4, salesPrice);
                pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Item Entered Successfully! ");
            } catch (SQLException ex) {
                 Logger.getLogger(GoodsListing.class.getName()).log(Level.SEVERE, null, ex);
            }

            clearFields();
            StockTable();
            goods_listing_table();
        }
        

    }//GEN-LAST:event_jButton13ActionPerformed

