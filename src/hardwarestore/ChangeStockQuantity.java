    private void SearchBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchBoxKeyReleased
      
        DefaultTableModel currtableModel = (DefaultTableModel) goods_listingjTable.getModel();//To empty the table before search
        currtableModel.setRowCount(0); //To search for contents from original table content
        
        for (Object rows : originalTableModel) {
            Vector rowVector = (Vector) rows;
            for (Object column : rowVector) {
                if (column.toString().contains(SearchBox.getText())) {
                    //content found so adding to table
                    currtableModel.addRow(rowVector);
                    break;
                }
            }

        }
       
       
    }//GEN-LAST:event_SearchBoxKeyReleased

    private void goods_listingjTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_goods_listingjTableMouseClicked

        
        jLabel7.setVisible(true);
        jTextField6.setVisible(true);
        jComboBox1.setVisible(true);
        jButton13.setEnabled(true);

        
         DefaultTableModel d1 = (DefaultTableModel)goods_listingjTable.getModel();
        int SelectIndex = goods_listingjTable.getSelectedRow();  
        
        itemNo = d1.getValueAt(SelectIndex, 0).toString();
        
        try {
            pst = con.prepareStatement("select *from stock where item_no = ?");
            pst.setString(1, itemNo);
            rs = pst.executeQuery();
            while(rs.next()){
                amount = Double.parseDouble(rs.getString("quantity_available"));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(GoodsListing.class.getName()).log(Level.SEVERE, null, ex);
        }
         
         description = d1.getValueAt(SelectIndex, 1).toString();
         cost = d1.getValueAt(SelectIndex, 2).toString();
         salesPrice = d1.getValueAt(SelectIndex, 3).toString();
    }//GEN-LAST:event_goods_listingjTableMouseClicked

